#Reading the file
import pandas as pd
df = pd.read_csv("/content/final_processed_job_dataset")

df.head()

df.shape

"""Creating Companies Table"""

companies = (df[["company", "industry"]].drop_duplicates().reset_index(drop=True))
companies["company_id"] = companies.index + 1

companies.head()

col_to_move = companies.pop('company_id')
companies.insert(0, 'company_id', col_to_move)

companies.head()

companies.shape

print(companies)

"""Creating Locations Table"""

locations = (df[["location"]].drop_duplicates().reset_index(drop=True))
locations["location_id"] = locations.index + 1

locations.head()

col_to_move = locations.pop('location_id')
locations.insert(0, 'location_id', col_to_move)

locations.head()

print(locations)

"""Creating Jobs Table"""

df.columns

#Mapping the companies with their company_id
company_map = dict(zip(companies["company"], companies["company_id"]))

df['company_id'] = df['company'].map(company_map)

#Mapping the locations with their location_id
location_map = dict(zip(locations["location"], locations["location_id"]))

df['location_id'] = df['location'].map(location_map)

df.columns

jobs = df.drop(columns = ['company', 'location', 'industry'])

jobs.head()

col_to_move = jobs.pop('salary')
jobs.insert(2, 'salary', col_to_move)

jobs.head()

"""Exporting the datasets"""

companies.to_csv("companies.csv", index=False)
locations.to_csv("locations.csv", index=False)
jobs.to_csv("jobs.csv", index=False)