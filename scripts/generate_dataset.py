"""Logic region to generate realistic jobs data points"""

from datetime import datetime
import random
def generate_jobs(job_id):

    #title
    title = random.choice(list(titleToCompanies.keys()))
    #company
    company = random.choice(titleToCompanies[title])

    #industry
    industry = company_to_industry[company]

    #employment_type
    employment_type = random.choice(["Internship", "Full-Time"])

    #Salary
    min, max = salary_ranges[title][employment_type]
    salary = round(random.uniform(min,max),1)

    #location
    location = random.choice(locations)

    #experience
    experience = random.choice([0,0.5]) if employment_type == "Internship" else random.choice([0,1,2])

    #skills
    skills = random.sample(skill_pools[title], random.randint(5,8))
    skills = ", ".join(skills)

    #work_mode
    work_mode = random.choice(["Remote", "On-site"])

    #posted_date
    day = random.randint(1,30)
    posted_date = datetime(2026, 6, day).strftime("%d-%b-%y")


    return {
        "job_id": job_id,
        "job_title": title,
        "company": company,
        "location": location,
        "experience": experience,
        "salary": salary,
        "skills": skills,
        "employment_type": employment_type,
        "industry": industry,
        "work_mode": work_mode,
        "posted_date": posted_date

    }
generated_jobs = []

for i in range(51, 501):
    generated_jobs.append(generate_jobs(i))
    print(generate_jobs(i))
